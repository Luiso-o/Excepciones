package n1ejercicio1;

public class Producto {
	
	//Atributos
	private String nombre; 
	private int precio;
	
	//constructor
	public Producto(String nombre, int precio) {
		this.nombre = nombre;
		this.precio = precio;
	}

	//getters
	public String getNombre() {
		return nombre;
	}

	public int getPrecio() {
		return precio;
	}


	
}

